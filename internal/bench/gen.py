#!/usr/bin/env python3
"""Generate bench_sizes_test.go: small/medium/large configs benchmarked across
every library, sonic-style. Field types cycle string/int/bool for realism."""

SIZES = {"Small": 5, "Medium": 30, "Large": 120}

def field(i):
    kind = i % 5
    if kind == 3:
        return f"F{i}", "int", f'"K{i}"'
    if kind == 4:
        return f"F{i}", "bool", f'"K{i}"'
    return f"F{i}", "string", f'"K{i}"'

def value(i):
    kind = i % 5
    if kind == 3:
        return f"K{i}={1000 + i}"
    if kind == 4:
        return f"K{i}=true"
    return f"K{i}=value_{i}"

def struct_def(name, n):
    lines = [f"type Cfg{name} struct {{"]
    for i in range(n):
        f, t, key = field(i)
        tag = f'`env:{key} envconfig:{key} mapstructure:{key}`'
        lines.append(f"\t{f} {t} {tag}")
    lines.append("}")
    return "\n".join(lines)

def sample(n):
    return "\\n".join(value(i) for i in range(n)) + "\\n"

out = []
out.append("// Code generated by gen.py; DO NOT EDIT.")
out.append("package bench")
out.append("")
out.append("import (")
out.append("\t\"context\"")
out.append("\t\"strings\"")
out.append("\t\"testing\"")
out.append("")
out.append("\t\"github.com/bakhod1r/oneenv\"")
out.append("\tenv \"github.com/caarlos0/env/v11\"")
out.append("\t\"github.com/ilyakaznacheev/cleanenv\"")
out.append("\t\"github.com/joeshaw/envdecode\"")
out.append("\t\"github.com/joho/godotenv\"")
out.append("\tkelsey \"github.com/kelseyhightower/envconfig\"")
out.append("\tsethvargo \"github.com/sethvargo/go-envconfig\"")
out.append("\t\"github.com/spf13/viper\"")
out.append(")")
out.append("")

for name, n in SIZES.items():
    out.append(struct_def(name, n))
    out.append("")
    out.append(f'const sample{name} = "{sample(n)}"')
    out.append("")

# library -> (bench func name, body template using {T}=struct, {S}=sample const)
LIBS = [
    ("Oneenv", '''\t\tdata := []byte(sample{name})
\t\tfor i := 0; i < b.N; i++ {{
\t\t\tvar cfg Cfg{name}
\t\t\tif err := oneenv.Unmarshal(data, &cfg); err != nil {{
\t\t\t\tb.Fatal(err)
\t\t\t}}
\t\t}}'''),
    ("Caarlos0", '''\t\tfor i := 0; i < b.N; i++ {{
\t\t\tm, _ := godotenv.Parse(strings.NewReader(sample{name}))
\t\t\tvar cfg Cfg{name}
\t\t\tif err := env.ParseWithOptions(&cfg, env.Options{{Environment: m}}); err != nil {{
\t\t\t\tb.Fatal(err)
\t\t\t}}
\t\t}}'''),
    ("Sethvargo", '''\t\tctx := context.Background()
\t\tfor i := 0; i < b.N; i++ {{
\t\t\tm, _ := godotenv.Parse(strings.NewReader(sample{name}))
\t\t\tvar cfg Cfg{name}
\t\t\tif err := sethvargo.ProcessWith(ctx, &sethvargo.Config{{Target: &cfg, Lookuper: sethvargo.MapLookuper(m)}}); err != nil {{
\t\t\t\tb.Fatal(err)
\t\t\t}}
\t\t}}'''),
    ("Viper", '''\t\tfor i := 0; i < b.N; i++ {{
\t\t\tv := viper.New()
\t\t\tv.SetConfigType("env")
\t\t\tif err := v.ReadConfig(strings.NewReader(sample{name})); err != nil {{
\t\t\t\tb.Fatal(err)
\t\t\t}}
\t\t\tvar cfg Cfg{name}
\t\t\tif err := v.Unmarshal(&cfg); err != nil {{
\t\t\t\tb.Fatal(err)
\t\t\t}}
\t\t}}'''),
    ("Kelsey", '''\t\tfor i := 0; i < b.N; i++ {{
\t\t\tm := loadSample(sample{name})
\t\t\tvar cfg Cfg{name}
\t\t\tif err := kelsey.Process("", &cfg); err != nil {{
\t\t\t\tb.Fatal(err)
\t\t\t}}
\t\t\tunset(m)
\t\t}}'''),
    ("Envdecode", '''\t\tfor i := 0; i < b.N; i++ {{
\t\t\tm := loadSample(sample{name})
\t\t\tvar cfg Cfg{name}
\t\t\tif err := envdecode.Decode(&cfg); err != nil {{
\t\t\t\tb.Fatal(err)
\t\t\t}}
\t\t\tunset(m)
\t\t}}'''),
    ("Cleanenv", '''\t\tfor i := 0; i < b.N; i++ {{
\t\t\tm := loadSample(sample{name})
\t\t\tvar cfg Cfg{name}
\t\t\tif err := cleanenv.ReadEnv(&cfg); err != nil {{
\t\t\t\tb.Fatal(err)
\t\t\t}}
\t\t\tunset(m)
\t\t}}'''),
]

for lib, body in LIBS:
    out.append(f"func BenchmarkSizes{lib}(b *testing.B) {{")
    for name in SIZES:
        out.append(f'\tb.Run("{name}", func(b *testing.B) {{')
        out.append("\t\tb.ReportAllocs()")
        out.append(body.format(name=name))
        out.append("\t})")
    out.append("}")
    out.append("")

with open("bench_sizes_test.go", "w") as f:
    f.write("\n".join(out) + "\n")
print("wrote bench_sizes_test.go")
